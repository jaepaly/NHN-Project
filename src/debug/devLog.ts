/**
 * 개발 전용 진단 로그 — 프로덕션에선 완전히 사라진다.
 *
 * 제출물 ①은 "플레이 가능한 빌드 + 소스"라 심사위원이 devtools를 연다.
 * 런 전환·컨트롤 적용·오토 비중 같은 내부 계측이 콘솔에 흐르면 완성도가 깎이고,
 * 컨트롤 로그는 주문 적중마다 객체를 직렬화해 부하도 준다.
 *
 * 프로덕션에선 아무것도 출력하지 않는다. 다만 **인자는 호출 전에 평가**되므로,
 * 적중마다 도는 핫패스에는 쓰지 말고 호출부를 `if (import.meta.env.DEV)`로 직접
 * 감싸 객체 생성까지 제거할 것 (예: ProtoScene의 `[Control]` 로그).
 * 실패·경고(에셋 로드 실패 등)는 프로덕션에서도 보여야 하므로 여기 태우지 않는다.
 */
export function devInfo(...args: unknown[]): void {
  if (import.meta.env.DEV) console.info(...args);
}
